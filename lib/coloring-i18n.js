// Reverse lookup: URL slug → { lang, enTopic }
function buildSlugIndex() {
  const idx = {};
  for (const [lang, cfg] of Object.entries(LANGS)) {
    idx[cfg.root] = { lang, type: 'hub' };
    for (const [enTopic, slug] of Object.entries(cfg.topicSlugs)) {
      idx[`${cfg.root}/${slug}`] = { lang, type: 'topic', enTopic };
    }
  }
  return idx;
}

