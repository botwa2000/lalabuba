function updateTopicLinks(lang) {
  const root = COLORING_ROOTS[lang] || 'coloring-pages';
  // Update explore nav button href to the correct language hub
  const exploreBtn = document.getElementById('explore-nav-btn');
  if (exploreBtn) {
    const enHub = lang === 'en' ? '/en/coloring-pages/' : `/${root}/`;
    exploreBtn.href = enHub;
  }
  document.querySelectorAll('[data-topic]').forEach(el => {
    const topic = el.dataset.topic;
    const slug  = COLORING_TOPIC_SLUGS[topic]?.[lang] || topic;
    el.href = `/${root}/${slug}/`;
    const span = el.querySelector('span');
    if (span) span.textContent = COLORING_TOPIC_NAMES[topic]?.[lang] || span.textContent;
  });
  const todayLink = document.getElementById('hero-today-link');
  if (todayLink) {
    todayLink.href = lang === 'de' ? '/ausmalbilder/heute/' : '/coloring-pages/today/';
    todayLink.textContent = COLORING_TODAY_LABELS[lang] || COLORING_TODAY_LABELS.en;
  }
  // Update URL bar to reflect language (enables link sharing in correct language)
  // Preserve any share/query params (e.g. ?s=1&q=dinosaur&d=easy) so loadFromShare() can read them.
  const langPath = lang === 'en' ? '/' : `/${lang}/`;
  if (location.pathname === '/' || /^\/[a-z]{2,3}\/$/.test(location.pathname)) {
    history.replaceState({}, '', langPath + location.search);
  }
}

// Auto-detect browser language on first visit, fall back to 'en'
function detectLang() {
  const saved = localStorage.getItem('lalabuba-lang');
  if (saved && SUPPORTED_LANGS.includes(saved)) return saved;
  const browser = (navigator.language || 'en').split('-')[0].toLowerCase();
  return SUPPORTED_LANGS.includes(browser) ? browser : 'en';
}

let currentLang = detectLang();

export function getCurrentLang() { return currentLang; }

export function t(key, ...args) {
  const val = TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.en[key];
  return typeof val === 'function' ? val(...args) : (val ?? key);
}

export function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('lalabuba-lang', lang);
  applyTranslations();
  updateTopicLinks(lang);
}

export function applyTranslations() {
  updateTopicLinks(currentLang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    el.setAttribute('aria-label', t(el.dataset.i18nAriaLabel));
  });

  // Update language picker button to show current flag + code
  const activeOption = document.querySelector(`.lang-option[data-lang="${currentLang}"]`);
  const flagEl = document.getElementById('lang-current-flag');
  const codeEl = document.getElementById('lang-current-code');
  if (flagEl) flagEl.dataset.lang = currentLang;   // CSS gradient keyed off data-lang
  if (activeOption && codeEl) codeEl.textContent = activeOption.dataset.code;
  document.querySelectorAll('.lang-option').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });

  document.documentElement.lang = currentLang;
}
